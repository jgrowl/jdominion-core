package jdominion.card;

public class CardProperties {

	private static final String CARD_SETS_DIR_PATH = "card-sets";

	private static final String CARDS_DIR_NAME = "cards";

	private static final String CARD_FILE_NAME = "card.xml";

	public static final String getCardSetsDirPath() {
		return CARD_SETS_DIR_PATH;
	}

	public static final String getCardFileName() {
		return CARD_FILE_NAME;
	}

	public static final String getCardsDirName() {
		return CARDS_DIR_NAME;
	}

}
